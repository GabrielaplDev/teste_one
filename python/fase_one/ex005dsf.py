# Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e ser antecessor.

n = int(input('Digite um número: '))
a = n - 1
s = n + 1

print('O antecessor é {} \nÉ o sucessor é {}'.format(a, s))

